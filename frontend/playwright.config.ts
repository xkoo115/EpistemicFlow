/**
 * Playwright E2E 测试配置文件
 *
 * 本配置用于 EpistemicFlow 前后端集成测试，支持：
 * - 自动启动前端开发服务器 (Vite)
 * - 自动启动后端 API 服务器 (FastAPI)
 * - 跨域 (CORS) 代理配置
 * - 多浏览器测试 (Chromium, Firefox, WebKit)
 * - 测试追踪与截图
 */

import { defineConfig, devices } from '@playwright/test';

/**
 * 环境变量配置
 * 可通过 process.env 覆盖默认值
 */
const isCI = !!process.env.CI;
const FRONTEND_PORT = process.env.FRONTEND_PORT || 5173;
const BACKEND_PORT = process.env.BACKEND_PORT || 8000;

/**
 * Playwright 配置
 */
export default defineConfig({
  // ============================================================================
  // 测试目录配置
  // ============================================================================

  /**
   * 测试文件根目录
   * 所有 E2E 测试文件放在 frontend/e2e 目录下
   */
  testDir: './e2e',

  /**
   * 测试文件匹配模式
   * 匹配所有 .spec.ts 或 .test.ts 文件
   */
  testMatch: /.*\.(spec|test)\.ts/,

  /**
   * 全局超时时间 (毫秒)
   * 整个测试套件的最大执行时间
   */
  timeout: 120 * 1000,

  /**
   * 单个测试超时时间 (毫秒)
   */
  expect: {
    /**
     * expect() 断言超时时间
     */
    timeout: 10 * 1000,
  },

  /**
   * 完全并行运行测试
   * 注意：如果测试之间有依赖关系，应禁用此选项
   */
  fullyParallel: false,

  /**
   * 是否在 CI 环境下失败时重试
   * CI 环境重试 2 次，本地不重试
   */
  retries: isCI ? 2 : 0,

  /**
   * 并行工作进程数
   * CI 环境使用 1 个进程，本地使用一半的 CPU 核心数
   */
  workers: isCI ? 1 : undefined,

  // ============================================================================
  // 报告器配置
  // ============================================================================

  /**
   * 测试报告器
   * - list: 命令行列表格式
   * - html: HTML 报告 (测试失败时生成)
   */
  reporter: isCI
    ? [['list'], ['html', { open: 'never', outputFolder: 'playwright-report' }]]
    : [['list'], ['html', { open: 'on-failure', outputFolder: 'playwright-report' }]],

  // ============================================================================
  // 全局设置
  // ============================================================================

  use: {
    /**
     * 基础 URL
     * 所有 page.goto() 调用都会基于此 URL
     */
    baseURL: `http://localhost:${FRONTEND_PORT}`,

    /**
     * 追踪配置
     * - on-first-retry: 仅在第一次重试时记录追踪
     * - on: 始终记录追踪 (用于调试)
     * - off: 不记录追踪
     */
    trace: isCI ? 'on-first-retry' : 'on',

    /**
     * 失败时截图
     */
    screenshot: 'only-on-failure',

    /**
     * 失败时录制视频
     */
    video: isCI ? 'on-first-retry' : 'off',

    /**
     * 浏览器上下文选项
     */
    // 忽略 HTTPS 证书错误 (用于本地开发)
    ignoreHTTPSErrors: true,

    // 导航超时时间
    navigationTimeout: 30 * 1000,

    // 操作超时时间 (click, fill 等)
    actionTimeout: 10 * 1000,
  },

  // ============================================================================
  // 项目配置 (多浏览器测试)
  // ============================================================================

  projects: [
    // --------------------------------------------------------------------------
    // Setup 项目：用于全局初始化 (如启动服务器)
    // --------------------------------------------------------------------------
    {
      name: 'setup',
      testMatch: /global-setup\.ts/,
    },

    // --------------------------------------------------------------------------
    // Chromium 浏览器测试
    // --------------------------------------------------------------------------
    {
      name: 'chromium',
      use: { ...devices['Desktop Chrome'] },
      dependencies: ['setup'],
    },

    // --------------------------------------------------------------------------
    // Firefox 浏览器测试 (仅本地运行)
    // --------------------------------------------------------------------------
    {
      name: 'firefox',
      use: { ...devices['Desktop Firefox'] },
      dependencies: ['setup'],
    },

    // --------------------------------------------------------------------------
    // WebKit (Safari) 浏览器测试 (仅本地运行)
    // --------------------------------------------------------------------------
    {
      name: 'webkit',
      use: { ...devices['Desktop Safari'] },
      dependencies: ['setup'],
    },

    // --------------------------------------------------------------------------
    // 移动端 Chrome 测试
    // --------------------------------------------------------------------------
    {
      name: 'Mobile Chrome',
      use: { ...devices['Pixel 5'] },
      dependencies: ['setup'],
    },

    // --------------------------------------------------------------------------
    // 移动端 Safari 测试
    // --------------------------------------------------------------------------
    {
      name: 'Mobile Safari',
      use: { ...devices['iPhone 12'] },
      dependencies: ['setup'],
    },
  ],

  // ============================================================================
  // 开发服务器配置
  // ============================================================================

  /**
   * Web Server 配置
   * Playwright 会在测试前自动启动服务器
   */
  webServer: [
    // --------------------------------------------------------------------------
    // 前端开发服务器 (Vite)
    // --------------------------------------------------------------------------
    {
      /**
       * 服务器命令
       * 使用 npm run dev 启动 Vite 开发服务器
       */
      command: 'npm run dev',

      /**
       * 服务器 URL
       * Playwright 会等待此 URL 可访问后才开始测试
       */
      url: `http://localhost:${FRONTEND_PORT}`,

      /**
       * 重用已有服务器
       * 如果服务器已在运行，则不重新启动
       */
      reuseExistingServer: !isCI,

      /**
       * 服务器启动超时时间 (毫秒)
       */
      timeout: 60 * 1000,

      /**
       * 工作目录
       * 命令执行的目录
       */
      cwd: '.',

      /**
       * 环境变量
       */
      env: {
        PORT: String(FRONTEND_PORT),
      },

      /**
       * 标准输出日志
       */
      stdout: 'pipe',
      stderr: 'pipe',
    },

    // --------------------------------------------------------------------------
    // 后端 API 服务器 (FastAPI)
    // --------------------------------------------------------------------------
    {
      /**
       * 后端启动命令
       * 使用 uvicorn 启动 FastAPI 服务器
       * 注意：需要在项目根目录执行此命令
       */
      command: `python -m uvicorn main:app --host 0.0.0.0 --port ${BACKEND_PORT} --reload`,

      /**
       * 后端服务器 URL
       * 使用 /docs 端点检查服务器是否就绪
       */
      url: `http://localhost:${BACKEND_PORT}/docs`,

      /**
       * 重用已有服务器
       */
      reuseExistingServer: !isCI,

      /**
       * 服务器启动超时时间 (毫秒)
       */
      timeout: 60 * 1000,

      /**
       * 工作目录
       * 后端项目根目录
       */
      cwd: '../',

      /**
       * 环境变量
       */
      env: {
        // 设置 Python 环境变量
        PYTHONUNBUFFERED: '1',
        // 数据库 URL (使用测试数据库)
        DATABASE_URL: 'sqlite+aiosqlite:///:memory:',
      },

      /**
       * 标准输出日志
       */
      stdout: 'pipe',
      stderr: 'pipe',
    },
  ],
});
